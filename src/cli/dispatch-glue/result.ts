import type { CliResult } from "./types.js";

export function ok(stdout: string): CliResult {
	return { exitCode: 0, stdout, stderr: "" };
}

export function fail(stderr: string): CliResult {
	return { exitCode: 1, stdout: helpText(), stderr };
}

